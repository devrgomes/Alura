let numero = parseInt(prompt("Digite um número inteiro:"));

if (numero ==0) {
    alert ("O número que você escolheu é ZERO!");
    console.log("O número que você escolheu é ZERO!");

} else {
    if (numero < 0) {
        alert ("O número que você escolheu NEGATIVO");
        console.log("O número que você escolheu é NEGATIVO!");

    } else {
        alert ("O número que você escolheu é POSITIVO!");
        console.log("O número que você escolheu é POSITIVO!");
    }
}
